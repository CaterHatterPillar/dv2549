#ifndef DV2549_EXPERIMENT_CASE_H
#define DV2549_EXPERIMENT_CASE_H

#include <Matrix.h>

template < class T >
struct Case {
	Case() {
		a = b = c = ref = nullptr;
		ms = -1;
		equalToRef = false;
	}
	~Case() {
		ASSERT_DELETE( a );
		ASSERT_DELETE( b );
		ASSERT_DELETE( c );
		ASSERT_DELETE( ref );
	}

	Matrix< T >* a;	// Matrices generated by matrixgen.
	Matrix< T >* b;	// Matrices generated by matrixgen.
	Matrix< T >* c;	// Result matrix calculated by experiment.
	Matrix< T >* ref; // Result matrix generated by matrixgen.

	double ms; // Execution time in ms.
	bool equalToRef;
};

#endif // DV2549_EXPERIMENT_CASE_H